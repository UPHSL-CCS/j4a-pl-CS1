// Tokens.cs
using System;

class TokensExample {
    static void Main() {
        // Examples of keywords, identifiers, and literals
        int number = 10;              // keyword: int, identifier: number, literal: 10
        string name = "Alice";        // keyword: string, identifier: name, literal: "Alice"
        double pi = 3.14;             // keyword: double, identifier: pi, literal: 3.14

        Console.WriteLine("Number: " + number);
        Console.WriteLine("Name: " + name);
        Console.WriteLine("Pi: " + pi);
    }
}
